/* bases.c -- выводит число 100 в десятично, восьмиричной и шестнадцатиричной форме */
#include <stdio.h>
int main(void)
{
  int x = 100;
  printf("Десятичное = %d; восьмиричное = %o; шестнадцатиричное = %x\n", x, x, x);
  printf("Десятичное = %d; восьмиричное = %#o; шестнадцатиричное = %#x\n", x, x, x);

  return 0;
}
